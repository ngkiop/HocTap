"""
1. Phép toán cơ bản

Nhập vào hai số nguyên a, b.

In ra: a + b, a - b, a * b, a / b, a // b, a % b.
"""

a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))

print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
